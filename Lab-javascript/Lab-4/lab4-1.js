"use strict";

function describeCountry(contryName,pop,cap){
    return `${contryName} has ${pop} million people and its capital city is ${cap}`;
}
console.log(describeCountry('vietnam',99,'hanoi'));
console.log(describeCountry('lao',40,'viengchan'));
console.log(describeCountry('campuchia',65,'phnom penh'));